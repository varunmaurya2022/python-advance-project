import math
x=1.51   #print nearest integer
y=-1.521
print(round(x))
print(round(y))
print("_________________________")
print(round(x,1)) #print 1 decimal place
print(round(y,2)) #print 2 decipmal place
print("____________________________")
print(math.floor(x))  #print largest value less than x
print(math.floor(y)) 
print("_________________________")
print(math.ceil(x)) #print the smallest integer greater than x.
print(math.ceil(y))
print("----------------------------") 
print(math.trunc(x)) #drop the decimal value only print integer
print(math.trunc(y))
  